using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Enigma
{
    internal class Keyboard
    {

        public string Forward(string letter)
        {
            string signal;
            signal = letter;

            return signal;
        }

        public string Backward(string signal)
        {
            string letter;
            letter = signal;

            return letter;
        }

    }
}
